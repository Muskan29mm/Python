# Change Dictionary Items

thisdict = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1976
}
print(thisdict)
thisdict["year"] = 1985
print(thisdict)

# Update dictionary

dict1 = {
    "brand": "Ford",
    "model": "Mustang",
    "year": 1964
}
print(dict1)
dict1.update({"year": 2000})
print(dict1)