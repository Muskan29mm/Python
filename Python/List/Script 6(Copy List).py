# Copy the list

# Use built-in copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()
print(mylist)

# Use built-in list() method
thislist1 = ["apple", "banana", "cherry"]
mylist1 = list(thislist1)
print(mylist1)

# use the slice operator
thislist2 = ["apple", "mango", "cherry", "banana"]
mylist2 = thislist2[:]
print(mylist2)

